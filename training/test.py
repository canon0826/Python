print("Hello World!")

# printとは出力関数であり表示したい文字列や数値などの値を画面に表示させる
# ()の中に値を入れると、その値を表示する

print(100-1)

# 出力結果
# 99

# 演算子の種類
# + 足し算
# * 掛け算
# - 引き算
# / 割り算
# // 割り算(少数部分を切り捨て)
# % 割り算の余り

print("答えは", 10+20)
# 出力結果
# 答えは 30

# 2文字以上は文字列という
# 「,(カンマ)」で区切ると複数の値を並べて表示できる

print("こんにちは、太郎さん。 ")
print("今日はいい天気ですね。 ")

# 出力結果
# こんにちは、太郎さん。
# 今日はいい天気ですね。
# for文 繰り返し処理

# for カウント変数 in range(回数)
#     繰り返す処理

# (例)5x0~5x9の10個の掛け算
# 繰り返す回数 0〜9の10回
# 繰り返す処理 5かける カウントの値 かけ算の答えを表示

for i in range(10):
    print(5,"x",i,"=",5*i)

# 出力結果
# 5 x 0 = 0
# 5 x 1 = 5
# 5 x 2 = 10
# 5 x 3 = 15
# 5 x 4 = 20
# 5 x 5 = 25
# 5 x 6 = 30
# 5 x 7 = 35
# 5 x 8 = 40
# 5 x 9 = 45